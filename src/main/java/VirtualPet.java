
public abstract class VirtualPet {
	int health;
	int boredom;
	String name;
	String description;
	
	public String getName() {
		return name; 
	}
	
	public String getDescription() {
		return description; 
	}
	
	public int getHealth() {
		return health; 
	}
	
	public int getBoredom() {
		return boredom; 
	}
}
